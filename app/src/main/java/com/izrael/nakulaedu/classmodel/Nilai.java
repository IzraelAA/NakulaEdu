package com.izrael.nakulaedu.classmodel;

public class Nilai {

    private String  id;
    private String  nama;
    private String  nilai;
    private String  pengetahuan;
    private String  pengajar;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getNilai() {
        return nilai;
    }

    public void setNilai(String nilai) {
        this.nilai = nilai;
    }

    public String getPengetahuan() {
        return pengetahuan;
    }

    public void setPengetahuan(String pengetahuan) {
        this.pengetahuan = pengetahuan;
    }

    public String getPengajar() {
        return pengajar;
    }

    public void setPengajar(String pengajar) {
        this.pengajar = pengajar;
    }
}
